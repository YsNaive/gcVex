#pragma onec

struct int2
{
    int x;
    int y;

    int2(){
        x = 0;
        y = 0;
    }
    int2(int xVal, int yVal){
        x = xVal;
        y = yVal;
    }
};

struct int3
{
    int x;
    int y;
    int z;

    int3(){
        x = 0;
        y = 0;
        z = 0;
    }
    int3(int xVal, int yVal, int zVal){
        x = xVal;
        y = yVal;
        z = zVal;
    }
};
